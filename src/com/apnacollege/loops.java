package com.apnacollege;

public class loops {
    public static void main(String args[]){
        for (int i=1;i<=20;i++){
            System.out.println(i);
        }
        int j=0;
        while(j<=20){
            System.out.println(j);
            j++;
        }
        int k=0;
        do {
            System.out.println(k);
            k++;
        }
        while (k<=20);
    }
}
